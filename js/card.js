const projectsContainer = document.querySelector('.web__items')
const loadMoreBtn = document.querySelector('#load-more')

let itemsToShow = 6

function renderProjects(data, isAppending = false) {
	if (!projectsContainer) return

	// Если это не дозагрузка, мы берем первые 6.
	// Если дозагрузка — берем следующую порцию (от текущего кол-ва до itemsToShow)
	const startIndex = isAppending ? projectsContainer.querySelectorAll('.project-card').length : 0
	const visibleProjects = data.slice(startIndex, itemsToShow)

	const html = visibleProjects
		.map(item => {
			const completedClass = item.isCompleted ? 'project-card--completed' : ''
			const tagsHtml = item.stack
				.map(tag => `<span class="project-card__tag">${tag}</span>`)
				.join('')

			const adaptiveText = item.isAdaptive ? 'ЕСТЬ' : 'НЕТ'
			const adaptiveClass = item.isAdaptive ? '' : 'project-card__status--error'
			const wpText = item.isWordpress ? 'ЕСТЬ' : 'НЕТ'
			const wpClass = item.isWordpress ? '' : 'project-card__status--error'

			return `
      <article class="project-card web__item ${completedClass}">
        <a href="${item.link || '#'}" class="project-card__link">
          <div class="project-card__header">
            <div class="project-card__dots">
              <span class="project-card__dot project-card__dot--red" title='Закрыть'></span>
              <span class="project-card__dot project-card__dot--orange" title='Свернуть'></span>
              <span class="project-card__dot project-card__dot--green" title='Развернуть'></span>
            </div>
						<div class="project-card__address-bar "></div>
          </div>
          <div class="project-card__preview">
            <img src="${item.img}" alt="${item.title}" class="project-card__img" loading="lazy" />
          </div>
          <div class="project-card__body">
            <span class="project-card__badge">Вариант ${item.variant}</span>
            <h3 class="project-card__title">${item.title}</h3>
            <div class="project-card__look">
              <div class="project-card__look-title">Смотреть проект</div>
            </div>
          </div>
          <div class="project-card__footer">
            <div class="project-card__stack">
						<div class="project-card__stack-title">Стек</div>
              <div class="project-card__stack-tag">${tagsHtml}</div>
            </div>
            <div class="project-card__info">
              <div class="project-card__status ${adaptiveClass}">
                <div class="project-card__status-text">Адаптив: ${adaptiveText}</div>
              </div>
              <div class="project-card__status ${wpClass}">
                <div class="project-card__status-text">WordPress: ${wpText}</div>
              </div>
            </div>
          </div>
        </a>
      </article>`
		})
		.join('')

	if (isAppending) {
		// 🔥 ДОБАВЛЯЕМ новые в конец, не трогая старые
		projectsContainer.insertAdjacentHTML('beforeend', html)
	} else {
		// Полная замена (только при первом запуске или фильтрации)
		projectsContainer.innerHTML = html
	}

	// 🔥 Анимируем ТОЛЬКО новые карточки (у которых нет класса show)
	setTimeout(() => {
		const newCards = projectsContainer.querySelectorAll('.project-card:not(.show)')
		newCards.forEach((card, index) => {
			setTimeout(() => {
				card.classList.add('show')
			}, index * 80)
		})
	}, 50)

	if (itemsToShow >= data.length) {
		loadMoreBtn.style.display = 'none'
	}
}

// ПРАВИЛЬНЫЙ ВЫЗОВ В ОБРАБОТЧИКЕ
loadMoreBtn.addEventListener('click', () => {
	itemsToShow += 6
	renderProjects(projects, true) // Передаем true, чтобы дозагружать
})

// Первый запуск
renderProjects(projects)
